package MusicStore;



public class RockDetails {

	String artistName;
	String songName;
	
	public RockDetails(String artistName, String songName) {
		this.artistName = artistName;
		this.songName = songName;
	
	}

	public String getArtistName() {
		return artistName;
	}

	public String getSongName() {
		return songName;
	}
	
}
